document.addEventListener('DOMContentLoaded', function() {
    try {
        // Buat layer vektor untuk Kabat menggunakan my_kabat
        const vectorKabat = L.geoJSON(my_kabat, {
            style: function(feature) {
                return {
                    fillColor: '#ff7800',
                    weight: 2,
                    opacity: 1,
                    color: 'white',
                    dashArray: '3',
                    fillOpacity: 0.7
                };
            },
            onEachFeature: function(feature, layer) {
                // Tambahkan popup saat mengklik fitur
                layer.on('click', function(e) {
                    const props = feature.properties;
                    const content = `
                        <table>
                            <tr><td>Nama Objek</td><td>${props.NAMOBJ || '-'}</td></tr>
                            <tr><td>Kode Zona</td><td>${props.KODZON || '-'}</td></tr>
                            <tr><td>Nama Zona</td><td>${props.NAMZON || '-'}</td></tr>
                            <tr><td>Kode Sub Zona</td><td>${props.KODSZN || '-'}</td></tr>
                            <tr><td>Nama Sub Zona</td><td>${props.NAMSZN || '-'}</td></tr>
                        </table>
                    `;
                    
                    L.popup()
                        .setLatLng(e.latlng)
                        .setContent(content)
                        .openOn(map);
                });
            }
        });

        // Tambahkan ke peta
        vectorKabat.addTo(map);
        
        console.log('Layer Kabat berhasil dimuat');
        
    } catch (error) {
        console.error('Error saat memproses my_kabat:', error);
    }
});